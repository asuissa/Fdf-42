#include "libft.h"

size_t		count_before(char const *s)
{
	int		count;
	int		i;

	i = 0;
	count = 0;
	while (s[i] != '\0' && (s[i] == ' ' || s[i] == '\t' || s[i] == '\n'))
	{
		count++;
		i++;
	}
	return (count);
}
